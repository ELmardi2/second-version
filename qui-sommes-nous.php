ARTICLE 6 - ADHÉSION A L&#39;ASSOCIATION

L&#39;adhésion de chaque nouveau membre est soumise à l&#39;acceptation du Conseil

d&#39;Administration ou du Bureau de l&#39;Association, et est réservée aux personnes physiques

âgées d&#39;au moins 16 ans, et aux personnes morales.

Toute personne, physique comme morale, doit accepter intégralement les statuts, et le cas

échéant le règlement intérieur, de l&#39;Association.

L&#39;adhésion à l&#39;Association est soumise au versement d&#39;une cotisation, dont le montant

sera fixé chaque année lors de l&#39;Assemblée Générale Ordinaire de l&#39;Association.

Les membres d&#39;honneur de l&#39;Association sont, en raison de leur qualité,

exceptionnellement dispensés de verser une cotisation à l&#39;Association.

ARTICLE 7 - PERTE DE LA QUALITÉ DE MEMBRE DE L&#39;ASSOCIATION

Le décès, la démission, le défaut de paiement de la cotisation annuelle après un premier

rappel ainsi que la radiation ou exclusion décidée par le conseil d&#39;administration entraînent

la perte de qualité de membre pour les personnes physiques.

La démission, la disparition, fusion, liquidation, le non-paiement de la cotisation annuelle

après un premier rappel, ainsi que la radiation ou exclusion décidée par le conseil

d&#39;administration entraînent la perte de qualité de membre pour les personnes morales.

La radiation d&#39;un membre peut intervenir, outre les cas susmentionnés, par décision

motivée du Conseil d&#39;Administration, pour des motifs graves et justifiés. Le membre visé

par la mesure de radiation est averti par courrier recommandé avec accusé de réception,

15 jours avant la prise de décision effective, afin de lui permettre de s&#39;expliquer devant

l&#39;organe de décision compétent. La mesure de radiation sera prise après audition du

membre visé.
