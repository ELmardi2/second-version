ARTICLE 8 - RESSOURCES DE L&#39;ASSOCIATION

Les ressources de l&#39;Association se composent :

- des droits d&#39;entrée et cotisations versés par ses membres ;

- des subventions de l&#39;Etat ou des collectivités publiques, comme les départements et les

communes ;

- des dons manuels et dons d&#39;utilité publique consentis par ses membres ou des tiers ;

- des revenus et intérêts générés par les biens, valeurs et droits lui appartenant ;

- de toute ressource autorisée par les lois et règlements en vigueur.

L&#39;Association tient une comptabilité annuelle faisant apparaître un compte de résultat, un

bilan et une annexe, conformément aux dispositions du règlement du 16 février 1999

relatif aux modalités d&#39;établissement des comptes annuels des associations et fondations,

établie dans les 6 mois qui suivent la clôture de l&#39;exercice annuel.

