ARTICLE 11 - CONSEIL D&#39;ADMINISTRATION

Le Conseil d&#39;Administration se compose d&#39;un corps de 6 membres, élus par l&#39;Assemblée

Générale Ordinaire, parmi ses membres agréés, pour une durée de 5 ans.

- Les membres du Conseil d&#39;Administration sortant sont rééligibles.

- Les membres fondateurs sont par ailleurs membres de droit du Conseil d&#39;Administration,

à titre permanent, incessible et intransmissible.

Le Conseil d&#39;Administration est en charge de la gestion de l&#39;Association et de la

préparation des travaux de l&#39;Assemblée Générale, dont il établit l&#39;ordre du jour et applique

