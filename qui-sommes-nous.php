adressé quinze jours à l&#39;avance, qui définira l&#39;ordre du jour.

Lors de la tenue de l&#39;Assemblée Générale Ordinaire, sont présentés aux membres :

- le rapport moral de l&#39;Association, remis par le Président ;

- le rapport d&#39;activité de l&#39;Association, remis par le Secrétaire Général ;

- le rapport financier de l&#39;Association comprenant le rapport de gestion et les comptes

annuels, remis par le Trésorier ;
