- renouveler les membres du Conseil d&#39;Administration si celui-ci est institué ;

- délibérer les points inscrits à l&#39;ordre du jour.

Le vote par procuration est autorisé, dans la mesure où le mandat est remis à un autre

membre de l&#39;Association.

L&#39;Assemblée Générale Ordinaire prend ses décisions à la majorité absolue des voix.

Le quorum exigé doit représenter au moins 70 % des membres de l&#39;Association.

Dans l&#39;hypothèse où ce quorum ne serait pas atteint, une nouvelle Assemblée Générale

