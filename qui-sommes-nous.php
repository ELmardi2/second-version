STATUTS DE L&#39;ASSOCIATION

REFUHELP STARTUP

Association soumise à la loi du 1er juillet 1901 et au décret du 16 août 1901

PREAMBULE

Constatant la pertinence des engagements des Etats en matière de protection des

demandeurs d’asile, des réfugiés, des bénéficiaires de la protection subsidiaire et des

apatrides,

Convaincu que le numérique est un des outils contemporain à grande capacité d’inclusion

sociale,

Mû par la volonté de renforcer l’insertion des personnes ci-haut visées dans leurs pays

d’accueil,

Nous, membres fondateurs penons la décision dont la teneur suit :

ARTICLE 1 - NOM DE L&#39;ASSOCIATION

En date du 06/04/2017, il est formé par les adhérents aux présents statuts et ceux qui y

adhéreront ultérieurement, une association régie par la loi du 1er juillet 1901 et le décret

du 16 août 1901, ayant pour titre : REFUHELP STARTUP

L&#39;Association pourra également être désignée par le sigle suivant : RSTART

C’est une association est laïque, apolitique, interculturelle, et intergénérationnelle.

ARTICLE 2 - OBJET DE L&#39;ASSOCIATION

L&#39;Association a pour objet :

- La vulgarisation du numérique ;

- La création des solutions numériques à usage des migrants, et/ou des entités

opérant sur cette question ;

- L’insertion professionnelle des migrants ;

- Contribuer à construire des échanges positifs entre les accueillants et les accueils

par tous.
