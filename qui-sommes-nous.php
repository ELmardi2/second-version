ARTICLE 13 – LES PÔLES

L’association est composée de deux pôles ; Le pôle numérique et le pôle insertion

professionnelle.

Chaque pôle est composé de 3 à 5 membres. Ils rendent compte de son activité à chaque

assemblée générale de l’association ou au conseil d’administration lorsqu’il le demande.

Chaque section peut gérer son propre budget de fonctionnement, celui-ci est intégré dans

la comptabilité générale de l’association. Le (la) trésorier(e) de la section doit rendre des

comptes réguliers au trésorier(e) de l’association qui est le responsable de l’ensemble du

budget.

La qualité de fondateur ou de co-fondateur de l’association est compatible avec la

fonction de membre d’un pôle.

ARTICLE 14 - INDEMNITÉS

Toutes les fonctions, y compris celles des membres du Conseil d&#39;administration et du

Bureau, sont gratuites et bénévoles.

Seuls les frais occasionnés par l&#39;accomplissement de leur mandat sont remboursés sur

présentation de justificatifs.

Le rapport financier présenté annuellement lors de l&#39;assemblée générale ordinaire de

l&#39;Association devra présenter, par bénéficiaire, les remboursements de frais de mission, de

déplacement ou de représentation.

Les dispositions de ce présent article pourront être affinées et précisées dans le règlement

intérieur de l&#39;Association.

ARTICLE 15 - RÈGLEMENT

Un règlement intérieur pourra être établi par le conseil d&#39;administration, qui devra être

approuvé par membres lors l&#39;Assemblée Générale de l&#39;Association.
